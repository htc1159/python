#!/usr/bin/env python3
# -*- coding: utf-8 -*- 
#取绝对值
print(abs(-3))


# 取最大值
print(max(1,-4,56,7))

# 数据类型转换
# 转为int
print(int('1'))

# 转为float
print(float('23.45'))

# 转为string
print(str('12.43'))

# 转为bool
# 0 '' "" () [] {} 只有空值时 才被认为是false  ‘false’ 表示不是一个空字符，所以为true
print(bool(1))

print(bool(''))


print(bool(0))


print(None)

# 计算hex
print (hex(255))

print(hex(1000))
